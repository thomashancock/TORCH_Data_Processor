// -----------------------------------------------------------------------------
// -----------------------------------------------------------------------------
Edge::Edge(
	const unsigned int tdcID,
	const unsigned int channelID,
	const unsigned int timestamp
) :
m_tdcID(tdcID),
m_channelID(channelID),
m_timestamp(timestamp),
m_fineTimestamp(timestamp%256)
{ }
// -----------------------------------------------------------------------------
//
// -----------------------------------------------------------------------------
bool Edge::operator< (
	const Edge& other
) const {
	return this->getTimestamp() < other.getTimestamp();
}

